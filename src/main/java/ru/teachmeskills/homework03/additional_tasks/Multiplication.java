package ru.teachmeskills.homework03.additional_tasks;

public class Multiplication {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
            for (int g = 1; g < 10; g++) {
                System.out.print(g * i + "\t");
            }
            System.out.println();
        }
    }
}
